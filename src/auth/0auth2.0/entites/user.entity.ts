import { ApiProperty } from '@nestjs/swagger';
import { Skill } from 'src/profile/entities/skills.entity';
import { Project } from 'src/projects/entities/projects.entity';
import {
  Column,
  Entity,
  ManyToMany,
  OneToMany,
  PrimaryGeneratedColumn,
} from 'typeorm';
import { Role } from '../enums';
@Entity()
export class User {
  @PrimaryGeneratedColumn()
  id: number;

  @ApiProperty({
    description: 'custom user id generated by uuid module',
  })
  @Column()
  userId: string;

  @ApiProperty({
    description: 'username of the user',
  })
  @Column()
  userName: string;

  @ApiProperty({
    description: 'first name',
  })
  @Column()
  firstName: string;

  @ApiProperty({
    description: 'last name',
  })
  @Column()
  lastName: string;

  @ApiProperty({
    description: 'position or designation',
  })
  @Column()
  position: string;

  @ApiProperty({
    description: 'major of study',
  })
  @Column()
  major: string;

  @ApiProperty({
    description: 'a bio explaining users intro',
  })
  @Column()
  bio: string;

  @ApiProperty({
    description: 'email of the user',
  })
  @Column()
  email: string;

  @ApiProperty({
    description: 'hashed password',
  })
  @Column()
  password: string;

  @ApiProperty({
    description: 'User Role',
  })
  @Column({
    type: 'enum',
    enum: Role,
    default: Role.VISIONARY,
  })
  role: Role;

  @ApiProperty({
    description: 'hashed refresh token',
  })
  @Column({
    default: null,
  })
  hashedRt: string;

  @ApiProperty({
    description: 'user verification status',
  })
  @Column({
    default: false,
  })
  isVerified: boolean;

  @OneToMany(() => Skill, (skill) => skill.skillForUser)
  skills: Skill[];

  @OneToMany(() => Project, (project) => project.projectOwner)
  ownerInProject: Project[];

  @ManyToMany(() => Project, (project) => project.contributerInProjects, {
    onDelete: 'CASCADE',
  })
  contributerInProject: Project[];
}
